
  create or replace  view DEVELOPER_SANDBOX.DBT_SNOWFLAKE_USAGE.V_WAREHOUSE_STG  as (
    
 

WITH source AS (
  SELECT * FROM  SNOWFLAKE.ACCOUNT_USAGE."QUERY_HISTORY"
),
rename as (
  SELECT DISTINCT 
  MD5(S.WAREHOUSE_ID) AS K_WAREHOUSE_DLHK 
  ,S.WAREHOUSE_ID AS K_WAREHOUSE_BK  
  ,S.WAREHOUSE_NAME AS A_WAREHOUSE_NAME  
FROM 
  source S
WHERE 
  S.WAREHOUSE_ID IS NOT NULL
     
)

SELECT * FROM rename
  );
